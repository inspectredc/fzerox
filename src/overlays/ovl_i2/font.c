void Font_IntToString(s32 num, s8* str) {
    s32 numDigits;
    s32 digitMask;
    s32 i;
    s32 j;

    j = num;
    numDigits = 0;
    if (num == 0) {
        numDigits = 1;
    } else {
        while (j != 0) {
            j /= 10;
            numDigits++;
        }
    }

    for (i = numDigits; i > 0; i--) {
        digitMask = 1;
        for (j = 0; j < i - 1; j++) {
            digitMask *= 10;
        }

        *str++ = (num / digitMask) + '0';
        num %= digitMask;
    }

    *str = '\0';
}

